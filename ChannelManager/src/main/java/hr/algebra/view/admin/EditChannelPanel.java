    private void init() {
        try {
            initValidation();
            hideErrors();
            initRepository();
            initTable();
        } catch (Exception ex) {
            Logger.getLogger(EditChannelPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Unrecoverable error", "Cannot initiate the form");
            System.exit(1);
        }
    }

    
    private void initValidation() {
        validationFields = Arrays.asList(tfCHTitle, tfCHLink, taCHDescription, tfCHPublishedDate, tfCHPicturePath);
        errorLabels = Arrays.asList(lbCHTitleError, lbCHLinkError, lbCHDescriptionError, lbCHDateError, lbCHPicturePathError);
    }
    
    private void hideErrors()
    {
        errorLabels.forEach(e -> e.setVisible(false));
    }
    
    private void initRepository() throws Exception {
        repository = RepositoryFactory.getRepository();
    }

    private void initTable() throws Exception {
        tbChannels.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tbChannels.setAutoCreateRowSorter(true);
        tbChannels.setRowHeight(25);
        channelTableModel = new ChannelTableModel(repository.selectChannels());
        tbChannels.setModel(channelTableModel);
    }
 
    
    private boolean formValid() {
        hideErrors();
        boolean ok = true;

        for (int i = 0; i < validationFields.size(); i++) {
            ok &= !validationFields.get(i).getText().trim().isEmpty();
            errorLabels.get(i).setVisible(validationFields.get(i).getText().trim().isEmpty());
            if ("Date".equals(validationFields.get(i).getName())) {
                try {
                    LocalDateTime.parse(validationFields.get(i).getText().trim(), Channel.DATE_FORMATTER);
                } catch (Exception e) {
                    ok = false;
                    errorLabels.get(i).setVisible(true);
                }
            }
        }
        return ok;
    }

    
    private void clearForm() {
        hideErrors();
        validationFields.forEach(e -> e.setText(""));
        lbCHImage.setIcon(new javax.swing.ImageIcon(getClass().getResource("/assets/no_image.png")));
        selectedChannel = null;
    }

    private String uploadPicture() throws IOException {
        String picturePath = tfCHPicturePath.getText();
        String ext = picturePath.substring(picturePath.lastIndexOf("."));
        String pictureName = UUID.randomUUID() + ext;
        String localPicturePath = DIR + File.separator + pictureName;

        FileUtils.copy(picturePath, localPicturePath);
        return localPicturePath;
    }
    
    private void showChannel() {
        clearForm();
        int selectedRow = tbChannels.getSelectedRow();
        int rowIndex = tbChannels.convertRowIndexToModel(selectedRow);
        int selectedChannelId = (int) channelTableModel.getValueAt(rowIndex, 0);

        try {
            Optional<Channel> optArticle = repository.selectChannel(selectedChannelId);
            if (optArticle.isPresent()) {
                selectedChannel = optArticle.get();
                fillForm(selectedChannel);
            }
        } catch (Exception ex) {
            Logger.getLogger(EditChannelPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to show article!");
        }
    }
    
    private void fillForm(Channel channel) {
        if (channel.getPicturePath() != null && Files.exists(Paths.get(channel.getPicturePath()))) {
            tfCHPicturePath.setText(channel.getPicturePath());
            setIcon(lbCHImage, new File(channel.getPicturePath()));
        }
        tfCHTitle.setText(channel.getTitle());
        tfCHLink.setText(channel.getLink());
        taCHDescription.setText(channel.getDescription());
        tfCHPublishedDate.setText(channel.getPublishedDate().format(Channel.DATE_FORMATTER));
    }
    
    private void setIcon(JLabel label, File file) {
        try {
            label.setIcon(IconUtils.createIcon(file, label.getWidth(), label.getHeight()));
        } catch (IOException ex) {
            Logger.getLogger(EditChannelPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to set icon!");
        }
    }
    
