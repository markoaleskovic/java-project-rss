    private void btnAddChannelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddChannelActionPerformed
        // TODO add your handling code here:
        if (!formValid()) {
            return;
        }
        try {
            String localPicturePath = uploadPicture();
            Channel channel = new Channel(
                    tfCHTitle.getText().trim(),
                    tfCHLink.getText().trim(),
                    taCHDescription.getText().trim(),
                    localPicturePath,
                    LocalDateTime.parse(tfCHPublishedDate.getText().trim(), Channel.DATE_FORMATTER)
            );
            repository.createChannel(channel);
            channelTableModel.setChannels(repository.selectChannels());
            clearForm();
            MessageUtils.showInformationMessage("Success", "Channel added");
        } catch (Exception ex) {
            Logger.getLogger(EditChannelPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to create article!");
        }
    }//GEN-LAST:event_btnAddChannelActionPerformed

    private void btnUpdateChannelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateChannelActionPerformed
        // TODO add your handling code here:
        if (selectedChannel == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose article to update");
            return;
        }
        if (!formValid()) {
            return;
        }
        try {
            if (!tfCHPicturePath.getText().trim().equals(selectedChannel.getPicturePath())) {
                if (selectedChannel.getPicturePath() != null) {
                    Files.deleteIfExists(Paths.get(selectedChannel.getPicturePath()));
                }
                String localPicturePath = uploadPicture();
                selectedChannel.setPicturePath(localPicturePath);
            }

            selectedChannel.setTitle(tfCHTitle.getText().trim());
            selectedChannel.setLink(tfCHLink.getText().trim());
            selectedChannel.setDescription(taCHDescription.getText().trim());
            selectedChannel.setPublishedDate(LocalDateTime.parse(tfCHPublishedDate.getText().trim(), Channel.DATE_FORMATTER));

            repository.updateChannel(selectedChannel.getId(), selectedChannel);
            channelTableModel.setChannels(repository.selectChannels());
            clearForm();
            MessageUtils.showInformationMessage("Success", "Channel updated");
        } catch (Exception ex) {
            Logger.getLogger(EditChannelPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Error", "Unable to update article!");
        }

    }//GEN-LAST:event_btnUpdateChannelActionPerformed

    private void btnDeleteChannelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteChannelActionPerformed
        // TODO add your handling code here:
        if (selectedChannel == null) {
            MessageUtils.showInformationMessage("Wrong operation", "Please choose article to delete");
            return;
        }
        if (MessageUtils.showConfirmDialog(
                "Delete article",
                "Do you really want to delete article?")) {
            try {
                if (selectedChannel.getPicturePath() != null) {
                    Files.deleteIfExists(Paths.get(selectedChannel.getPicturePath()));
                }
                repository.deleteChannel(selectedChannel.getId());
                channelTableModel.setChannels(repository.selectChannels());
                clearForm();
                MessageUtils.showInformationMessage("Success", "Channel deleted");
            } catch (Exception ex) {
                Logger.getLogger(EditChannelPanel.class.getName()).log(Level.SEVERE, null, ex);
                MessageUtils.showErrorMessage("Error", "Unable to delete article!");
            }
        }
    }//GEN-LAST:event_btnDeleteChannelActionPerformed

