    private void btnUploadChannelsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadChannelsActionPerformed
        new Thread(() -> {
            ext = cbCHExtension.getSelectedItem().toString().toLowerCase();
            try {
                channels.addAll(ChannelRssParser.parseChannelItems(ext));
                repository.createChannels(channels);
                loadModel();

            } catch (Exception ex) {
                MessageUtils.showErrorMessage("Unrecoverable error", "Unable to upload articles");
                System.exit(1);
            }}).start();
        
    }//GEN-LAST:event_btnUploadChannelsActionPerformed

