    private DefaultListModel<Channel> channelModel;
    private Repository repository;
    private String ext;
    
    private void init() {
        try {
            repository = RepositoryFactory.getRepository();
            channelModel = new DefaultListModel<>();
            loadModel();
        } catch (Exception ex) {
            Logger.getLogger(UploadChannelPanel.class.getName()).log(Level.SEVERE, null, ex);
            MessageUtils.showErrorMessage("Unrecoverable error", "Cannot initiate the form");
            System.exit(1);
        }
    }
    
    private void loadModel() throws Exception {
        channels = repository.selectChannels();
        
        EventQueue.invokeLater(() -> {    
            channelModel.clear();
            channels.forEach(channelModel::addElement);
            lsChannels.setModel(channelModel);
        });
    }


